package Three;

public class BitOperator {
    public static void main(String[] args) {
        int a = 5;
        int b = 7;
        int bitwiseOr = a|b;
        System.out.println(bitwiseOr);
        int bitwiseAnd = a&b;
        System.out.println(bitwiseAnd);
        int bitwiseNot = ~a;
        System.out.println(bitwiseNot);
        int bitwiseXor = a^b;
        System.out.println(bitwiseXor);
        
    }
}
