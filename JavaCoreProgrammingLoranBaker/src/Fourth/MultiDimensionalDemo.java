package Fourth;

public class MultiDimensionalDemo {
    public static void main(String[] args) {
        String[][] imePrezimenaArray = {
            {"Hodzic", "Nikolic", "Orucevic"},
            {"Ammar", "Marko", "Bakir"}
        };
        System.out.println(imePrezimenaArray[0][0] + " "+imePrezimenaArray[1][0]);
        System.out.println(imePrezimenaArray[0][1] + " " + imePrezimenaArray[1][1]);
        System.out.println(imePrezimenaArray[0][2] + " " + imePrezimenaArray[1][2]);
    }
            
}
